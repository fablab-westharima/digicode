const SUPPORTED: readonly Locale[] = ['ja', 'en', 'es', 'pt-PT', 'zh-TW'];
const DEFAULT_LOCALE: Locale = 'ja';

/**
 * Resolve Accept-Language header to a supported locale.
 * - 完全一致 'pt-PT' / 'zh-TW' を最優先
 * - prefix match 'pt' → 'pt-PT', 'zh' → 'zh-TW' を fallback として許容
 * - 'ja' / 'en' / 'es' は 2 文字一致
 * - 不一致時は DEFAULT_LOCALE 'ja'
 */
export function resolveLocale(acceptLanguage: string | undefined | null): Locale {
  if (!acceptLanguage) return DEFAULT_LOCALE;
  // Parse Accept-Language: "ja,en-US;q=0.9,en;q=0.8"
  const tags = acceptLanguage
    .split(',')
    .map((s) => {
      const [tag, qStr] = s.trim().split(';q=');
      const q = qStr ? parseFloat(qStr) : 1.0;
      return { tag: tag.trim(), q };
    })
    .filter((x) => x.tag)
    .sort((a, b) => b.q - a.q);

  for (const { tag } of tags) {
    // 完全一致
    if ((SUPPORTED as readonly string[]).includes(tag)) return tag as Locale;
    // 大文字小文字違いの完全一致 (例: 'pt-pt' / 'zh-tw')
    const norm = tag.toLowerCase();
    for (const s of SUPPORTED) {
      if (s.toLowerCase() === norm) return s;
    }
    // prefix match: 'pt' → 'pt-PT', 'zh' → 'zh-TW'
    const lang = tag.split('-')[0].toLowerCase();
    if (lang === 'pt') return 'pt-PT';
    if (lang === 'zh') return 'zh-TW';
    if (lang === 'ja') return 'ja';
    if (lang === 'en') return 'en';
    if (lang === 'es') return 'es';
  }
  return DEFAULT_LOCALE;
}

/**
 * Get translated message for a key + locale.
 * Falls back to ja if missing in target locale.
 */
export function getMessage(locale: Locale, key: ErrorKey): string {
  const bundle = errorMessages[locale] ?? errorMessages[DEFAULT_LOCALE];
  return bundle[key] ?? errorMessages[DEFAULT_LOCALE][key];
}
